    // ========== –£–õ–£–ß–®–ï–ù–ù–´–ô –ö–û–†–†–ï–ö–¢–û–† –¢–ï–ö–°–¢–ê ==========
    function correctText(text) {
        if (!text || text === '–ù–∞—á–Ω–∏—Ç–µ —Å–æ–±–∏—Ä–∞—Ç—å –æ—Ç–≤–µ—Ç...') return text;
        
        let corrected = text;
        
        // 1. –ë–ê–ó–û–í–ê–Ø –û–ß–ò–°–¢–ö–ê
        corrected = corrected.trim();
        
        // 2. –ò–°–ü–†–ê–í–õ–ï–ù–ò–ï –†–ê–°–ü–†–û–°–¢–†–ê–ù–Å–ù–ù–´–• –û–®–ò–ë–û–ö
        const words = corrected.split(/(\s+|[.!?‚Ä¶,;:])/);
        for (let i = 0; i < words.length; i++) {
            const word = words[i].toLowerCase().trim();
            if (commonMistakes[word]) {
                // –°–æ—Ö—Ä–∞–Ω—è–µ–º –æ—Ä–∏–≥–∏–Ω–∞–ª—å–Ω—ã–π —Ä–µ–≥–∏—Å—Ç—Ä –ø–µ—Ä–≤–æ–π –±—É–∫–≤—ã
                const isUpperCase = words[i][0] === words[i][0].toUpperCase();
                words[i] = isUpperCase 
                    ? commonMistakes[word].charAt(0).toUpperCase() + commonMistakes[word].slice(1)
                    : commonMistakes[word];
            }
        }
        corrected = words.join('');
        
        // 3. –ò–°–ü–†–ê–í–õ–ï–ù–ò–ï –ü–†–û–ë–ï–õ–û–í
        corrected = corrected.replace(/\s+/g, ' ');
        corrected = corrected.replace(/\s+([,.!?:;])/g, '$1');
        corrected = corrected.replace(/([,.!?:;])([^\s])/g, '$1 $2');
        
        // 4. –ö–ê–í–´–ß–ö–ò –ò –¢–ò–†–ï
        corrected = corrected.replace(/"([^"]*)"/g, '¬´$1¬ª');
        corrected = corrected.replace(/\s-(\s)/g, ' ‚Äî$1');
        
        // 5. –¢–†–û–ï–¢–û–ß–ò–ï
        corrected = corrected.replace(/\.{3,}/g, '‚Ä¶');
        
        // 6. –ó–ê–ì–õ–ê–í–ù–´–ï –ë–£–ö–í–´ (–ù–û–í–ê–Ø –í–ï–†–°–ò–Ø)
        const sentences = corrected.split(/([.!?‚Ä¶]+\s*)/);
        for (let i = 0; i < sentences.length; i++) {
            let sentence = sentences[i];
            if (sentence.length > 0 && !sentence.match(/^[.!?‚Ä¶]/)) {
                // –ù–∞—Ö–æ–¥–∏–º –ø–µ—Ä–≤—É—é –±—É–∫–≤—É –≤ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–∏
                for (let j = 0; j < sentence.length; j++) {
                    if (/[–∞-—èa-z]/i.test(sentence[j])) {
                        sentence = sentence.substring(0, j) + 
                                  sentence[j].toUpperCase() + 
                                  sentence.substring(j + 1);
                        break;
                    }
                }
                sentences[i] = sentence;
            }
        }
        corrected = sentences.join('');
        
        // 7. –ó–ê–ì–õ–ê–í–ù–´–ï –ü–û–°–õ–ï ! –ò ?
        corrected = corrected.replace(/([!?‚Ä¶]+\s+)([–∞-—è])/g, (match, p1, p2) => {
            return p1 + p2.toUpperCase();
        });
        
        // 8. –¢–û–ß–ö–ê –í –ö–û–ù–¶–ï
        if (corrected.length > 0 && !corrected.match(/[.!?‚Ä¶]$/)) {
            corrected += '.';
        }
        
        // 9. –§–ò–ù–ê–õ–¨–ù–ê–Ø –ó–ê–ß–ò–°–¢–ö–ê
        corrected = corrected.replace(/\s+([.!?‚Ä¶])/g, '$1');
        corrected = corrected.replace(/([.!?‚Ä¶])([–∞-—èa-z])/g, '$1 $2');
        corrected = corrected.replace(/\s+/g, ' ');
        
        return corrected;
    }

